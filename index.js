const numberConvertor = (value) => {
  if (+value > 999999) {
    return (
      value.slice(0, value.length - 6) +
      " " +
      value.slice(value.length - 6, value.length - 3) +
      " " +
      value.slice(value.length - 3)
    );
  } else if (+value > 999)
    return (
      value.slice(0, value.length - 3) + " " + value.slice(value.length - 3)
    );
  else return value;
};

